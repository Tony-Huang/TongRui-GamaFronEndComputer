    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        // TODO add your handling code here:
        char[] pwdChars = this.pwd.getPassword();
        String pwd = new String(pwdChars);

        if (this.checkPwd()) {
            CatAccUser user = SystemInfo.getCurrentUser();
            user.setPassword( EncryptUtil.encryptToMD5(pwd));//user.setPassword( pwd );
            try {
              
                UserService us = ServiceLocator.getUserService();
                us.update(user);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "本地数据库访问出错,请重试!");
               
            }
        }
    }//GEN-LAST:event_okBtnActionPerformed

