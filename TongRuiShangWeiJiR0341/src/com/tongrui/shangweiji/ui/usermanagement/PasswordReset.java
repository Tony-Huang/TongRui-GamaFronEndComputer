      boolean  checkPwd() {
        boolean res = true;
        String pwd = this.pwd.getText();
        String confirmPwd = this.confirmPwd.getText();
        if (pwd == null || confirmPwd == null || pwd.equals("") || confirmPwd.equals("")) {

            PwdWarning pwr = PwdWarning.getInstance(this, "密码不能空！");
            pwr.setVisible(true);
            return false;
        } else if (!pwd.equals(confirmPwd)) {
            PwdWarning pwr = PwdWarning.getInstance(this, "密码不一致！");
            pwr.setVisible(true);
            return false;
        } else if(pwd.length()<6) {
            PwdWarning pwr = PwdWarning.getInstance(this, "密码不能少于6位！");
            pwr.setVisible(true);
            return false;
        }
        else {
           //密码由字母、数字、下划线组成。
           Pattern p = Pattern.compile("\\w+");
           Matcher m = p.matcher(pwd);
           if (!m.matches()) {
               PwdWarning pwr = PwdWarning.getInstance(this, "密码非法！");
               pwr.setVisible(true);
               return false;
           }
       }

        return res;
    }
      
   
