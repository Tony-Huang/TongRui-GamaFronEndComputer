    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if (jToggleButton1.isSelected()) { //just pressed
            if (jRadioButton5.isSelected()) { //source close
                ctrlSvcImplObj.sendCommand(Constant.MANUL_T2Z2_ROTATE);
                System.out.println("sendCommand(Constant.MANUL_T2Z2_ROTATE)");
            }
            else {   //Some collimate No. is selected
                String st = jTextField5.getText().trim();
                boolean inputValid = CommonMethod.checkDoubleFromTextField(st,true);
                if (!inputValid) {
                    jToggleButton1.setSelected(false);
                    return;
                }
                double value = Double.valueOf(st);

                int colliNumber;
                if(jRadioButton9.isSelected()) colliNumber = 4;
                else if(jRadioButton8.isSelected()) colliNumber = 3;
                else if(jRadioButton7.isSelected()) colliNumber = 2;
                else colliNumber = 1;
                
                ctrlSvcImplObj.sendCommand(Constant.MANUL_T2Z2_ROTATE, value, colliNumber);
                System.out.println("sendCommand(Constant.MANUL_T2Z2_ROTATE, " + value + "; " + colliNumber);
            }
        } else {                         //just release
            ctrlSvcImplObj.sendCommand(Constant.MANUL_AXILES_STOP);
            System.out.println("sendCommand(Constant.MANUL_AXILES_STOP)");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

