    class ViewRefresh extends Thread {

        private SourcePartManual obj = null;

        public ViewRefresh(SourcePartManual obj_) {
            this.obj = obj_;
        }

        public void run() {
                while (true) {
                    collimaterValidityCheck();
               try {
                    obj.viewUpdate();
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                    log.error(null, ex);
                }
            }
        }
    }

    private void collimaterValidityCheck() {
        if(jRadioButton2.isSelected()) {
            jRadioButton5.setEnabled(true);
            jRadioButton6.setEnabled(true);
            jRadioButton7.setEnabled(true);
            jRadioButton8.setEnabled(true);
            jRadioButton9.setEnabled(true);
            
            if(jRadioButton5.isSelected()) {
                jLabel2.setEnabled(false);
                jTextField5.setEnabled(false);
                jLabel5.setEnabled(false);
            }
            else {
                jLabel2.setEnabled(true);
                jTextField5.setEnabled(true);
                jLabel5.setEnabled(false);
            }
        }
        else {
            jRadioButton5.setEnabled(false);
            jRadioButton6.setEnabled(false);
            jRadioButton7.setEnabled(false);
            jRadioButton8.setEnabled(false);
            jRadioButton9.setEnabled(false);
            jLabel2.setEnabled(false);
            jTextField5.setEnabled(false);
            jLabel5.setEnabled(false);
        }
    }
    
    private int cycles = 0;
    private static ControllerSvcImpl ctrlSvcImplObj = ControllerSvcImpl.getInstance();
    private void viewUpdate() {
        CurePolar curePolar = ctrlSvcImplObj.getCurePolar();
        LowComputerInfo lowComputerInfo = ctrlSvcImplObj.getLowComputerInfo();
        jTextField1.setText(String.valueOf(curePolar.getSourcePosition_T2()));
        jTextField2.setText(String.valueOf(curePolar.getCollimatorPosition_Z2()));
        
        CureInfo cureInfo = ctrlSvcImplObj.getCureInfo();
        jTextField6.setText(Double.valueOf(cureInfo.getRadioTimer1()).toString());
        jTextField7.setText(Double.valueOf(cureInfo.getRadioTimer2()).toString());
        
        if (isMovingButtonValid()) {
            cycles++; //wys.TBRemoved
            if (cycles >= 5 && lowComputerInfo.getMovingState() == 0) { //Moving action is end
                restoreEnable2ValidGroup();
                releaseAllToggleButtons();
                System.out.println("Moving is end.");
            }
        } else {
            cycles = 0;
        }
        
        cureHeadState1.update_date(curePolar);
        collimateState1.update_date(curePolar);
    }
    
    boolean isMovingButtonValid() {
        return (jToggleButton4.isSelected() || jToggleButton5.isSelected() || jToggleButton6.isSelected() || jToggleButton7.isSelected());
    }

